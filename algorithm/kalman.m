function Correct(data)
  global Covariance;
  global State;
  global F;
  global H;
  global Q;
  global R;
  global X0;
  global P0;
 
  #time update - prediction
  X0 = F*State;
  P0 = F*Covariance*F + Q;

  #measurement update - correction
  K = H*P0/(H*P0*H + R);
  State = X0 + K*(data - H*X0);
  Covariance = (1 - K*H)*P0;
endfunction;

